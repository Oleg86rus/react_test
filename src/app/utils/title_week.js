export const title_week = (i) => {
	switch (i) {
		case 1:
			return '01 Sep - 07 Sep'
		case 2:
			return '08 Sep - 14 Sep'
		case 3:
			return '15 Sep - 21 Sep'
		case 4:
			return '22 Sep - 28 Sep'
		case 5:
			return '29 Sep - 4 Oct'
		case 6:
			return '5 Oct - 11 Oct'
		case 7:
			return '12 Oct - 18 Oct'
	}
}